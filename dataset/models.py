class BackgroundImages(models.Model):
    """
    background images to paste generated cards and make new dataset
    """
    name = models.CharField(
        max_length=255, null=True, blank=True,
        help_text="name of background image ex. old_paper, table_bg etc..",
    )
    bg_image = models.ImageField(upload_to="background_images", null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.name 

class Dataset(models.Model):
    """
    generated dataset

    After creating the metadata and verifying the preview of card generation, you can generate dataset of completed task
    
    TODO: Get status information
    """
    name = models.CharField(
        max_length=255, null=True, blank=True,
        help_text="name of dataset ex. pan_card_format_1_10k",
    )
    desc = models.TextField(
        blank=True, null=True,
        help_text="Description of dataset"
    )
    desc_html = models.TextField(
        blank=True, null=True,
        help_text="Description of dataset in html format"
    )
    task = models.ForeignKey(
        Tasks, blank=True, on_delete=models.DO_NOTHING,
        help_text="Task id in which this dataset belongs"
    )
    count = models.IntegerField(
        blank=True, null=True,
        default=5,
        help_text="how many images to generate"
    )
    visualize_line_annotations = models.BooleanField(
        default=True, null=True, blank=True,
        help_text="Whether to visualize line annotations or not"
    )
    visualize_word_annotations = models.BooleanField(
        default=True, null=True, blank=True,
        help_text="Whether to visualize word level annotations or not"
    )
    is_pasted = models.BooleanField(
        default=False, null=True, blank=True,
        help_text="Is this dataset generated by pasting it in new backgrounds"
    )
    is_augmented = models.BooleanField(
        default=False, null=True, blank=True,
        help_text="Is this dataset generated from augmentation"
    )
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.name 


class DatasetPastedBgMeta(models.Model):
    """
    Store the extra information if dataset pasted on background images and new dataset generated
    """
    dataset = models.ForeignKey(
        Dataset, blank=True, on_delete=models.DO_NOTHING,
        related_name="dataset",
        help_text="Dataset of pasted images"
    )
    reference_dataset = models.ForeignKey(
        Dataset, blank=True, on_delete=models.DO_NOTHING,
        related_name="reference_dataset",
        help_text="the source dataset that has been used to generate this dataset"
    )
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.dataset.name 


